ResetStress = false

RegisterServerEvent("virus-hudcomponent:Server:UpdateStress")
AddEventHandler('virus-hudcomponent:Server:UpdateStress', function(StressGain)
	local src = source
    local Player = CTRPFW.Functions.GetPlayer(src)
    local newStress
    if Player ~= nil then
        if not ResetStress then
            if Player.PlayerData.metadata["stress"] == nil then
                Player.PlayerData.metadata["stress"] = 0
            end
            newStress = Player.PlayerData.metadata["stress"] + StressGain
            if newStress <= 0 then newStress = 0 end
        else
            newStress = 0
        end
        if newStress > 100 then
            newStress = 100
        end
        Player.Functions.SetMetaData("stress", newStress)
		TriggerClientEvent("hud:client:UpdateStress", src, newStress)
	end
end)

RegisterServerEvent('virus-hudcomponent:Server:GainStress')
AddEventHandler('virus-hudcomponent:Server:GainStress', function(amount)
    local src = source
    local Player = CTRPFW.Functions.GetPlayer(src)
    local newStress
    if Player ~= nil then
        if not ResetStress then
            if Player.PlayerData.metadata["stress"] == nil then
                Player.PlayerData.metadata["stress"] = 0
            end
            newStress = Player.PlayerData.metadata["stress"] + amount
            if newStress <= 0 then newStress = 0 end
        else
            newStress = 0
        end
        if newStress > 100 then
            newStress = 100
        end
        Player.Functions.SetMetaData("stress", newStress)
        TriggerClientEvent("hud:client:UpdateStress", src, newStress)
        TriggerClientEvent('CTRPFW:Notify', src, 'Increased stress', 'primary', 1500)
	end
end)

RegisterServerEvent('virus-hudcomponent:Server:RelieveStress')
AddEventHandler('virus-hudcomponent:Server:RelieveStress', function(amount)
    local src = source
    local Player = CTRPFW.Functions.GetPlayer(src)
    local newStress
    if Player ~= nil then
        if not ResetStress then
            if Player.PlayerData.metadata["stress"] == nil then
                Player.PlayerData.metadata["stress"] = 0
            end
            newStress = Player.PlayerData.metadata["stress"] - amount
            if newStress <= 0 then newStress = 0 end
        else
            newStress = 0
        end
        if newStress > 100 then
            newStress = 100
        end
        Player.Functions.SetMetaData("stress", newStress)
        TriggerClientEvent("hud:client:UpdateStress", src, newStress)
        TriggerClientEvent('CTRPFW:Notify', src, 'Decreased stress')
	end
end)
